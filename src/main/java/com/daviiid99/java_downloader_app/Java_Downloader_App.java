    public void updateTextFields(String action) {
        
        if (action.equals("download")){
            this.url = urlField.getText();
            this.fileName = fileField.getText();
            this.extension = extensionField.getText();
        }
        
        else {
            this.url = "";
            this.fileName = "";
            this.extension = "";
            this.path = "";
            urlField.setText("");
            fileField.setText("");
            extensionField.setText("");
            pathField.setText("");
           
        }
        
    }
    
     public void downloadFile(){
        // Default values
      OutputStream ops = null; // Default ops output stream
      InputStream ins = null; // Default ins
      String fileUrl = this.url; // File name
      String filePath = this.path; // Download path
      
      try {
          URL url = new URL(this.url);
          URLConnection connection = url.openConnection();
          ins = connection.getInputStream();
          ops = new FileOutputStream(filePath + "/" + this.fileName + this.extension);
          final byte[] bt = new byte[1024];
          int len;
          System.out.println("Descargando");
          
          while ((len = ins.read(bt)) != -1){
	   ops.write(bt, 0, len);}
      }catch (IOException ex){
	   ex.printStackTrace();
           System.out.println("error");
      }
    }
    
    public void selectFolder() {
    final JFileChooser selector = new JFileChooser(); // Crea un objeto de tipo selector
    final JDialog dialog = new JDialog();  
    
    selector.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
    int result = selector.showOpenDialog(dialog);
    
    selector.setCurrentDirectory(new java.io.File("."));  // Establecer el directorio seleccionado
    selector.setDialogTitle("Elige donde guardar el archivo"); // Titulo de la ventana

    this.path =  selector.getSelectedFile().getAbsolutePath();
    
    }


